package com.ssd;

public class Divisible {

	public static void main(String[] args) {
	int x=100;
	for(int i=1;i<=x;i++) {
		if(i%2==0) {
			System.out.println("divisible by 2 numbers list:"+i);
		}
		if(i%5==0) {
			System.out.println("divisible by 5 numbers list:"+i);
		}
	}

	}

}
